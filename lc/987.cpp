struct node {
    int val, x, y;
    friend bool operator<(const node &a, const node &b) {
        if (a.y != b.y)
            return a.y < b.y;
        else if (a.x != b.x)
            return a.x < b.x;
        else
            return a.val < b.val;
    }
};
class Solution {
  public:
    vector<node> V;
    void dfs(TreeNode *now, int x, int y) {
        V.push_back({now->val, x, y});
        if (now->left)
            dfs(now->left, x + 1, y - 1);
        if (now->right)
            dfs(now->right, x + 1, y + 1);
    }
    vector<vector<int>> verticalTraversal(TreeNode *root) {
        dfs(root, 0, 0);
        sort(V.begin(), V.end());
        vector<vector<int>> ret;
        int pre = -INF;
        vector<int> now;
        for (auto &i : V) {
            if (i.y != pre) {
                if (!now.empty()) {
                    ret.push_back(now);
                    now.clear();
                }
                pre = i.y;
                now.push_back(i.val);
            } else {
                now.push_back(i.val);
            }
        }
        if (!now.empty()) {
            ret.push_back(now);
            now.clear();
        }
        return ret;
    }
};

