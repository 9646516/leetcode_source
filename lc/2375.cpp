class Solution {
  public:
    string str;
    string sb;
    string ans = "9999999999999999";
    void dfs(int pos, int mask) {
        if (pos == str.size() + 1) {
            ans = min(ans, sb);
            return;
        }
        for (int i = 1; i <= 9; i++) {
            if ((mask >> i) & 1)
                continue;
            else {
                if (pos > 0) {
                    if (str[pos - 1] == 'I') {
                        if (sb[pos - 1] - '0' > i)
                            continue;
                    } else {
                        if (sb[pos - 1] - '0' < i)
                            continue;
                    }
                }
                sb.push_back('0' + i);
                dfs(pos + 1, mask | 1 << i);
                sb.pop_back();
            }
        }
    }
    string smallestNumber(const string &pattern) {
        str = pattern;
        dfs(0, 0);
        return ans;
    }
};

