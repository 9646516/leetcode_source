#include <ext/pb_ds/assoc_container.hpp>
#include <ext/pb_ds/priority_queue.hpp>
using namespace __gnu_pbds;
class Solution {
  public:
    vector<int> V[100005];
    vector<int> nums;
    vector<int> ret;
    set<int> dfs(int x, int fa) {
        set<int> q;
        q.insert(nums[x]);
        int cur = 1;
        for (int i : V[x]) {
            if (i != fa) {
                auto st = dfs(i, x);
                if (q.size() < st.size()) {
                    swap(q, st);
                }
                for (int j : st)
                    q.insert(j);
                cur = max(cur, ret[i]);
            }
        }
        while (q.count(cur)) {
            cur++;
        }
        ret[x] = cur;
        return q;
    }
    vector<int> smallestMissingValueSubtree(vector<int> &parents, vector<int> &nums) {
        for (int i = 0; i < (int)parents.size(); i++) {
            int u = i, v = parents[i];
            if (v == -1)
                continue;
            V[u].push_back(v);
            V[v].push_back(u);
        }
        ret.resize(nums.size());
        this->nums = move(nums);
        dfs(0, -1);
        return ret;
    }
};

