class Twitter {
  public:
    int idx = 0;
    deque<pair<int, int>> V[505];
    set<int> st[505];
    Twitter() {}

    void postTweet(int userId, int tweetId) {
        V[userId].push_back({idx++, tweetId});
        if (V[userId].size() > 10) {
            V[userId].pop_front();
        }
    }

    vector<int> getNewsFeed(int userId) {
        multiset<pair<int, int>> res;
        for (auto i : V[userId]) {
            res.insert(i);
            if (res.size() > 10)
                res.erase(res.begin());
        }
        for (auto j : st[userId]) {
            for (auto i : V[j]) {
                res.insert(i);
                if (res.size() > 10)
                    res.erase(res.begin());
            }
        }
        vector<int> ret;
        for (auto i : res) {
            ret.push_back(i.second);
        }
        reverse(ret.begin(), ret.end());
        return ret;
    }

    void follow(int followerId, int followeeId) { st[followerId].insert(followeeId); }

    void unfollow(int followerId, int followeeId) { st[followerId].erase(followeeId); }
};

