class Solution {
  public:
    uint64_t H[100000 + 555];
    uint64_t POW[100000 + 555];
    string longestPrefix(string s) {
        POW[0] = 1;
        for (int i = 1; i <= s.size(); i++)
            POW[i] = POW[i - 1] * 233;
        H[0] = s[0];
        for (int i = 1; i < s.size(); i++)
            H[i] = H[i - 1] * 233 + s[i];
        for (int i = s.size() - 1; i >= 1; i--) {
            uint64_t h = H[i - 1];
            uint64_t h2 = H[s.size() - 1] - H[s.size() - 1 - i] * POW[i];
            if (h == h2)
                return s.substr(0, i);
        }
        return "";
    }
};

