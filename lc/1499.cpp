class Solution {
  public:
    int findMaxValueOfEquation(vector<vector<int>> &points, int k) {
        deque<int> idx;
        multiset<int, greater<int>> st;
        int R = 0;
        int ans = numeric_limits<int>::min();
        for (int i = 0; i < (int)points.size(); i++) {
            debug(i, R);
            while (R != points.size()) {
                if (points[R][0] - points[i][0] <= k) {
                    idx.push_back(points[R][0] + points[R][1]);
                    st.insert(points[R][0] + points[R][1]);
                    R++;
                } else {
                    break;
                }
            }
            st.erase(st.lower_bound(idx[0]));
            idx.pop_front();
            debug(st);
            if (!st.empty()) {
                ans = max(ans, *st.begin() - points[i][0] + points[i][1]);
            }
        }
        return ans;
    }
};

