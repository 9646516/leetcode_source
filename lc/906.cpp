class Solution {
  public:
    int superpalindromesInRange(string left, string right) {
        ll L = stoll(left);
        ll R = stoll(right);
        int ans = 0;
        auto gao = [&](string val) {
            ll p = stoll(val);
            if (1.0 * p * p > R)
                return false;
            p = p * p;
            if (L <= p && p <= R) {
                string sb = to_string(p);
                for (int i = 0; i < sb.size(); i++) {
                    int r = sb.size() - 1 - i;
                    if (i > r)
                        break;
                    if (sb[i] != sb[r])
                        return true;
                }
                ans++;
            }
            return true;
        };
        for (int i = 0; i <= 1e5; i++) {
            string base = to_string(i);
            string inv = base;
            reverse(inv.begin(), inv.end());
            string c1 = base + inv;
            if (!gao(c1))
                break;
        }
        for (int i = 0; i <= 1e5; i++) {
            string base = to_string(i);
            string inv = base;
            reverse(inv.begin(), inv.end());
            base.pop_back();
            string c2 = base + inv;
            if (!gao(c2))
                break;
        }

        return ans;
    }
};

