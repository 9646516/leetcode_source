class Solution {
  public:
    int largestPalindrome(int n) {
        if (n == 1)
            return 9;
        ll base = 1; // 10^(n-1)
        for (int i = 1; i < n; i++)
            base *= 10;
        ll lo = base, hi = base * 10 - 1;
        for (ll i = hi;; i--) {
            ll full = i;
            ll now = i;
            while (now) {
                full = full * 10 + now % 10;
                now /= 10;
            }
            for (ll j = hi; j * j >= full; j--) {
                if (full % j == 0) {
                    ll f1 = full / j;
                    if (f1 >= lo && f1 <= hi)
                        return full % 1337;
                }
            }
        }
    }
};

