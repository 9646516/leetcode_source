#ifdef RINNE
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
#endif

class Solution {
  public:
    using node = ListNode;
    ListNode *oddEvenList(ListNode *head) {
        node *head1 = new node();
        node *tail1 = head1;

        node *head2 = new node();
        node *tail2 = head2;

        node *now = head;
        int ord = 1;
        while (now) {
            if (ord) {
                tail1->next = now;
                tail1 = tail1->next;
            } else {
                tail2->next = now;
                tail2 = tail2->next;
            }
            now = now->next;
            ord ^= 1;
        }
        tail1->next = nullptr;
        tail2->next = nullptr;
        now = head2->next;
        while (now) {
            tail1->next = now;
            tail1 = tail1->next;
            now = now->next;
        }
        tail2->next = nullptr;
        auto ret = head1->next;
        delete head1;
        delete head2;
        return ret;
    }
};

