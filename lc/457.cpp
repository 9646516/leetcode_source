class Solution {
  public:
    bool circularArrayLoop(vector<int> &nums) {
        const int N = nums.size();
        vector<int> vis(N, -1);
        for (int i = 0; i < (int)N; i++) {
            int now = i;
            vis[i] = i;
            while (true) {
                int next = (now + nums[now]) % N;
                next = (next + N) % N;
                if (nums[next] > 0 != nums[i] > 0 || next == now) {
                    break;
                } else {
                    if (vis[next] == i)
                        return 1;
                    else if (vis[next] != -1) {
                        return false;
                    }
                    vis[next] = i;
                    now = next;
                }
            }
        }
        return 0;
    }
};

