uint64_t H[2005];
uint64_t POW[2005];
class Solution {
  public:
    int distinctEchoSubstrings(string text) {
        H[0] = text[0];
        for (int i = 1; i < (int)text.size(); i++) {
            H[i] = H[i - 1] * 233 + text[i];
        }
        POW[0] = 1;
        for (int i = 1; i <= (int)text.size(); i++) {
            POW[i] = POW[i - 1] * 233;
        }
        auto get = [&](int l, int r) {
            uint64_t ret = H[r];
            if (l)
                ret -= H[l - 1] * POW[r - l + 1];
            return ret;
        };
        unordered_set<int> st;
        for (int i = 0; i < (int)text.size(); i++) {
            for (int j = 2; i + j - 1 < (int)text.size(); j += 2) {
                auto a = get(i, i + j / 2 - 1);
                auto b = get(i + j / 2, i + j - 1);
                if (a == b) {
                    st.insert(a);
                }
            }
        }
        return st.size();
    }
};

