class Solution {
  public:
    bool check(vector<int> &nums, int mid, int m) {
        int xsum = 0;
        int seg = 0;
        for (int i : nums) {
            if (i > mid)
                return false;
            if (i + xsum > mid) {
                seg++;
                xsum = i;
            } else {
                xsum += i;
            }
        }
        if (xsum)
            seg++;
        return seg <= m;
    }
    int splitArray(vector<int> &nums, int m) {
        ll sum = accumulate(nums.begin(), nums.end(), 0LL);
        ll L = 0, R = sum;
        for (int i = 0; i < m - 1; i++)
            R -= nums[i];
        for (int i = 0; i < m - 1; i++)
            R = max(R, 1LL * nums[i]);

        ll ans = R;
        while (L <= R) {
            int mid = (L + R) / 2;
            bool res = check(nums, mid, m);
            debug(mid, res);
            if (res) {
                ans = mid;
                R = mid - 1;
            } else {
                L = mid + 1;
            }
        }
        return ans;
    }
};

