ll xpow(ll a, ll b) {
    ll ret = 1;
    a %= MOD;
    assert(b >= 0);
    while (b) {
        if (b & 1)
            ret = ret * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return ret;
}
class Solution {
  public:
    int maxNiceDivisors(int primeFactors) {
        auto calc = [&](int x) {
            int a1 = primeFactors / x, b1 = primeFactors % x;
            //(a1+1)^b1*(a1)^(x-b1)
            // b1log(a1+1) + (x-b1)log(a1)
            return b1 * log(a1 + 1) + (x - b1) * log(a1);
        };
        int L = 1, R = primeFactors;
        while (L + 5 < R) {
            int rmid = (L + R) / 2;
            int lmid = (L + rmid) / 2;
            if (calc(lmid) > calc(rmid)) {
                R = rmid;
            } else {
                L = lmid;
            }
        }
        pair<double, int> ans = {-1, -1};
        for (int i = L; i <= R; i++)
            ans = max(ans, {calc(i), i});
        auto sel = ans.second;
        int a1 = primeFactors / sel, b1 = primeFactors % sel;
        ll a = xpow(a1 + 1, b1);
        ll b = xpow(a1, sel - b1);
        return a * b % MOD;
    }
};

