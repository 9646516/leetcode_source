class Solution {
  public:
    bool isInterleave(string s1, string s2, string s3) {
        if (s1.size() + s2.size() != s3.size())
            return false;
        else {
            static int dp[2][105][105];
            memset(dp, -1, sizeof(dp));
            function<bool(int, int, int)> gao = [&](int x, int y, int turn) -> bool {
                if (x == s1.size() && y == s2.size()) {
                    return true;
                } else if (dp[turn][x][y] != -1) {
                    return dp[turn][x][y];
                } else if (turn == 0) {
                    for (int i = x; i < (int)s1.size(); i++) {
                        if (s1[i] == s3[y + i]) {
                            if (gao(i + 1, y, turn ^ 1)) {
                                return dp[turn][x][y] = 1;
                            }
                        } else {
                            break;
                        }
                    }
                    return dp[turn][x][y] = false;
                } else {
                    for (int i = y; i < (int)s2.size(); i++) {
                        if (s2[i] == s3[x + i]) {
                            if (gao(x, i + 1, turn ^ 1)) {
                                return dp[turn][x][y] = 1;
                            }
                        } else {
                            break;
                        }
                    }
                    return dp[turn][x][y] = false;
                }
            };
            return gao(0, 0, 0) || gao(0, 0, 1);
        }
    }
};

