class Solution {
  public:
    int threeSumClosest(vector<int> &nums, int target) {
        sort(nums.begin(), nums.end());
        int ret = INF;
        for (int i = 0; i < (int)nums.size() - 2; i++) {
            for (int j = (int)nums.size() - 1; j > i + 1; j--) {
                debug(i, j);
                int need = target - nums[i] - nums[j];
                auto pos = lower_bound(&nums[i], &nums[j], need) - &nums[0];
                for (int k = -3; k <= 3; k++) {
                    int mix = pos + k;
                    if (mix > i && mix < j) {
                        if (abs(target - nums[i] - nums[j] - nums[mix]) < abs(target - ret)) {
                            ret = nums[i] + nums[j] + nums[mix];
                        }
                    }
                }
            }
        }
        return ret;
    }
};

