class Solution {
  public:
    string simplifyPath(string str) {
        string now;
        vector<string> V;
        for (int i = 0; i < (int)str.size(); i++) {
            if (str[i] == '/') {
                if (!now.empty()) {
                    V.push_back(now);
                    now.clear();
                }
            } else {
                now.push_back(str[i]);
            }
        }
        if (!now.empty()) {
            V.push_back(now);
        }
        vector<string> use;
        stringstream ss;

        for (auto &i : V) {
            if (i == ".") {
                continue;
            } else if (i == "..") {
                if (!use.empty())
                    use.pop_back();
            } else {
                use.push_back(i);
            }
        }
        for (auto &i : use)
            ss << "/" << i;
        auto ret = ss.str();
        if (ret.empty())
            ret.push_back('/');
        return ret;
    }
};

