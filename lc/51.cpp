class Solution {
  public:
    int len;
    vector<vector<string>> ret;
    Solution() = default;
    vector<int> put;
    void save_result() {
        vector<string> res;
        for (int i = 0; i < len; i++) {
            string now(len, '.');
            now[put[i]] = 'Q';
            res.push_back(now);
        }
        ret.push_back(res);
    }
    void dfs(int row, int col_mask, int X_mask, int X_mask2) {
        if (row == len) {
            save_result();
        } else {
            for (int i = 0; i < len; i++) {
                if (col_mask & (1 << i))
                    continue;
                int X = i - row + len;
                if (X_mask & (1 << X))
                    continue;
                int X2 = i + row;
                if (X_mask2 & (1 << X2))
                    continue;
                put.push_back(i);
                dfs(row + 1, col_mask | 1 << i, X_mask | 1 << X, X_mask2 | 1 << X2);
                put.pop_back();
            }
        }
    }
    vector<vector<string>> solveNQueens(int n) {
        len = n;
        dfs(0, 0, 0, 0);
        return ret;
    }
};

