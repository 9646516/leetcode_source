struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};
#endif

class Solution {
  public:
    unordered_map<const TreeNode *, int> dp[2][2];
    int dfs(const TreeNode *now, int cur, int fa) {
        if (!now)
            return 0;
        if (!now->left && !now->right) {
            if (cur == 0 && fa == 0)
                return INF;
            else
                return 0;
        }
        if (dp[cur][fa].count(now))
            return dp[cur][fa][now];
        else {
            int val = INF;
            for (int i = 0; i < 2; i++) {
                for (int j = 0; j < 2; j++) {
                    if (fa == 0 && cur == 0 && i == 0 && j == 0)
                        continue;
                    val = min(val, dfs(now->left, i, cur) + dfs(now->right, j, cur) + i + j);
                }
            }
            return dp[cur][fa][now] = val;
        }
    }
    int minCameraCover(TreeNode *root) { return min(dfs(root, 0, 0), dfs(root, 1, 0) + 1); }
};

