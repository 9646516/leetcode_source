class Solution {
  public:
    bool check(ll val, int k) {
        static int B[50];
        int ptr = 0;
        while (val) {
            B[ptr++] = val % k;
            val /= k;
        }
        int L = 0, R = ptr - 1;
        while (L <= R) {
            if (B[L] != B[R])
                return 0;
            L++;
            R--;
        }

        return 1;
    }
    long long kMirror(int k, int n) {
        ll ret = 0;
        for (ll L = 1; n > 0; L *= 10) {
            ll R = L * 10;
            for (int s = 1; s >= 0 && n > 0; s--) {
                for (ll j = L; j < R && n > 0; j++) {
                    static int B[50];
                    int ptr = 0;
                    ll now = j;
                    while (now) {
                        B[ptr++] = now % 10;
                        now /= 10;
                    }
                    now = j;
                    for (int o = s; o < ptr; o++) {
                        now = now * 10 + B[o];
                    }
                    if (check(now, k)) {
                        n--;
                        ret += now;
                    }
                }
            }
        }
        return ret;
    }
};

