int dp[105][105];
int cost[105][105];
class Solution {
  public:
    int r;
    int dfs(int l, int k) {
        if (k == 1) {
            return cost[l][r];
        } else if (l == r) {
            return INF;
        } else if (dp[l][k] != -1) {
            return dp[l][k];
        } else {
            int ans = INF;
            for (int i = l; i < r; i++) {
                ans = min(ans, cost[l][i] + dfs(i + 1, k - 1));
            }
            return dp[l][k] = ans;
        }
    }
    int palindromePartition(string s, int k) {
        r = s.size() - 1;
        memset(dp, -1, sizeof(dp));
        memset(cost, 0, sizeof(cost));
        for (int i = 2; i <= s.size(); i++) {
            for (int j = 0; i + j - 1 < (int)s.size(); j++) {
                int l = j, r = i + j - 1;
                if (s[l] == s[r])
                    cost[l][r] = cost[l + 1][r - 1];
                else
                    cost[l][r] = cost[l + 1][r - 1] + 1;
            }
        }
        return dfs(0, k);
    }
};

