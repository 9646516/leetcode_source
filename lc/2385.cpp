#ifdef RINNE

struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};
#endif

class Solution {
  public:
    vector<int> V[100005];
    int ans = 0;
    void dfs(TreeNode *x) {
        if (x->left) {
            V[x->val].push_back(x->left->val);
            V[x->left->val].push_back(x->val);
            dfs(x->left);
        }
        if (x->right) {
            V[x->val].push_back(x->right->val);
            V[x->right->val].push_back(x->val);
            dfs(x->right);
        }
    }
    void dfs2(int x, int fa, int dep) {
        ans = max(ans, dep);
        for (int i : V[x]) {
            if (i != fa) {
                dfs2(i, x, dep + 1);
            }
        }
    }
    int amountOfTime(TreeNode *root, int start) {
        dfs(root);
        dfs2(start, -1, 1);
        return ans - 1;
    }
};

