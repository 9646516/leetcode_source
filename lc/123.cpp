class Solution {
  public:
    int maxProfit(vector<int> &V) {
        vector<int> prefix;
        vector<int> suffix;
        {
            int mn = INF;
            int best = -INF;
            for (int i = 0; i < (int)V.size(); i++) {
                mn = min(mn, V[i]);
                best = max(V[i] - mn, best);
                prefix.push_back(best);
            }
        }
        {
            int mx = -INF;
            int best = -INF;
            for (int i = (int)V.size() - 1; i >= 0; i--) {
                mx = max(mx, V[i]);
                best = max(mx - V[i], best);
                suffix.push_back(best);
            }
            reverse(suffix.begin(), suffix.end());
        }
        int ans = prefix[(int)V.size() - 1];
        for (int i = 0; i + 1 < (int)V.size(); i++) {
            ans = max(prefix[i] + suffix[i + 1], ans);
        }
        debug(prefix);
        debug(suffix);
        return ans;
    }
};

