class RandomizedSet {
  public:
    RandomizedSet() {}

    bool insert(int val) {
        if (idx.count(val))
            return false;
        V.push_back(val);
        idx[val] = V.size() - 1;
        return true;
    }

    bool remove(int val) {
        if (!idx.count(val))
            return false;
        int place = idx[val];
        swap(V[place], V[V.size() - 1]);
        swap(idx[V[place]], idx[V[V.size() - 1]]);
        V.pop_back();
        idx.erase(val);
        return true;
    }

    int getRandom() {
        static mt19937 rng(random_device{}());
        uniform_int_distribution<int> uni(0, V.size() - 1);
        return V[uni(rng)];
    }
    vector<int> V;
    unordered_map<int, int> idx;
};

