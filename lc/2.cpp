struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
#endif

class Solution {
  public:
    ListNode *addTwoNumbers(ListNode *l1, ListNode *l2) {
        using node = ListNode;
        node *head = new node();
        node *now = head;
        int lbit = 0;
        bool fst = true;
        while (l1 || l2 || lbit) {
            if (!fst) {
                now->next = new node();
                now = now->next;
            } else {
                fst = false;
            }
            int val1 = l1 ? l1->val : 0;
            int val2 = l2 ? l2->val : 0;
            int val = val1 + val2 + lbit;
            debug(val);
            int left = val % 10;
            lbit = val / 10;
            now->val = left;
            if (l1)
                l1 = l1->next;
            if (l2)
                l2 = l2->next;
        }
        return head;
    }
};

