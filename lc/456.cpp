class Solution {
  public:
    bool find132pattern(vector<int> &nums) {
        multiset<int> st;
        for (int i : nums)
            st.insert(i);
        st.erase(st.lower_bound(nums[0]));
        vector<int> prefix_min = nums;
        for (int i = 1; i < (int)prefix_min.size(); i++) {
            prefix_min[i] = min(prefix_min[i], prefix_min[i - 1]);
        }

        for (int i = 1; i < (int)nums.size() - 1; i++) {
            st.erase(st.lower_bound(nums[i]));
            if (prefix_min[i - 1] < nums[i]) {
                int lo = prefix_min[i - 1] + 1;
                int hi = nums[i] - 1;
                auto ptr = st.lower_bound(lo);
                if (ptr != st.end() && (*ptr <= hi))
                    return true;
            }
        }
        return false;
    }
};

