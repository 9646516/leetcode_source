class Solution {
  public:
    int minOperations(vector<int> &numbers) {
        auto V = numbers;
        vector<int> s2(numbers.size());
        vector<int> s3(numbers.size());
        for (int i = 0; i < (int)V.size(); i++) {
            while (V[i] % 2 == 0) {
                V[i] /= 2;
                s2[i]++;
            }
            while (V[i] % 3 == 0) {
                V[i] /= 3;
                s3[i]++;
            }
        }
        for (int i : V) {
            if (i != V[0]) {
                return -1;
            }
        }
        int c2=*max_element(s2.begin(),s2.end());
        int c3=*max_element(s3.begin(),s3.end());
        int ans=0;
        for(int i:s2)ans+=c2-i;
        for(int i:s3)ans+=c3-i;
        return ans;
    }
};

