class Solution {
  public:
    string_view ss;
    NestedInteger solve(int l, int r) {
        if (ss[l] == '[') {
            l++;
            r--;
            NestedInteger ret{};
            while (l <= r) {
                if (ss[l] == ',')
                    l++;
                else if (ss[l] == '[') {
                    int L = l, R = r;
                    int cnt = 1;
                    for (int i = L + 1; i <= r; i++) {
                        if (ss[i] == '[')
                            cnt++;
                        if (ss[i] == ']')
                            cnt--;
                        if (cnt == 0) {
                            R = i;
                            break;
                        }
                    }
                    ret.add(solve(L, R));
                    l = R + 1;
                } else {
                    int pos = 1;
                    if (ss[l] == '-') {
                        l++;
                        pos = 0;
                    }
                    int now = 0;
                    while (l <= r && ss[l] != ',') {
                        now = now * 10 + ss[l] - '0';
                        l++;
                    }
                    ret.add(pos ? now : -now);
                }
            }
            return ret;
        } else {
            int pos = 1;
            if (ss[l] == '-') {
                l++;
                pos = 0;
            }
            int now = 0;
            for (int i = l; i <= r; i++)
                now = now * 10 + ss[i] - '0';
            return NestedInteger(pos ? now : -now);
        }
    }
    NestedInteger deserialize(string s) {
        ss = s;
        return solve(0, ss.size() - 1);
    }
};

