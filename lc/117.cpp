class Node {
  public:
    int val;
    Node *left;
    Node *right;
    Node *next;

    Node() : val(0), left(NULL), right(NULL), next(NULL) {}

    Node(int _val) : val(_val), left(NULL), right(NULL), next(NULL) {}

    Node(int _val, Node *_left, Node *_right, Node *_next)
        : val(_val), left(_left), right(_right), next(_next) {}
};
#endif
class Solution {
  public:
    Node *connect(Node *root) {
        Node *HEAD = new Node();
        Node *HEAD2 = new Node();
        Node *q = HEAD;
        Node *q2 = HEAD2;
        Node *q2_tail = HEAD2;
        q->next = root;
        while (q && q->next) {
            q = q->next;
            while (q) {
                if (q->left) {
                    q2_tail = q2_tail->next = q->left;
                }
                if (q->right) {
                    q2_tail = q2_tail->next = q->right;
                }
                q = q->next;
            }
            q = HEAD;
            HEAD->next = HEAD2->next;
            q2_tail = HEAD2;
            HEAD2->next = nullptr;
        }
        delete HEAD;
        delete HEAD2;
        return root;
    }
};

