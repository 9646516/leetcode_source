int deg[50000 + 555];
int ts[50000 + 555];
class Solution {
  public:
    int minimumTime(int n, vector<vector<int>> &relations, vector<int> &time) {
        vector<int> V[50000 + 555];
        vector<int> V2[50000 + 555];
        memset(deg, 0, sizeof(int) * n);
        for (auto &i : relations) {
            int u = i[0] - 1;
            int v = i[1] - 1;
            V[u].push_back(v);
            V2[v].push_back(u);
            deg[v]++;
        }
        queue<int> q;
        for (int i = 0; i < n; i++) {
            if (deg[i] == 0)
                q.push(i);
        }
        memset(ts, 0, sizeof(int) * n);
        int ans = 0;
        while (!q.empty()) {
            int now = q.front();
            q.pop();
            for (int i : V2[now]) {
                ts[now] = max(ts[now], ts[i] + time[i]);
            }
            ans = max(ans, ts[now] + time[now]);
            for (int i : V[now]) {
                deg[i]--;
                if (deg[i] == 0) {
                    q.push(i);
                }
            }
        }
        return ans;
    }
};

