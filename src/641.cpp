class MyCircularDeque {
  public:
    int *src;
    int L = 0;
    int sz = 0;
    int k;
    MyCircularDeque(int k) {
        src = new int[k];
        this->k = k;
    }

    bool insertFront(int value) {
        if (sz == k) {
            return 0;
        }
        L = (L - 1 + k) % k;
        src[L] = value;
        sz++;
        return 1;
    }

    bool insertLast(int value) {
        if (sz == k) {
            return 0;
        }
        int pos = (L + sz) % k;
        src[pos] = value;
        sz++;
        return 1;
    }

    bool deleteFront() {
        if (sz == 0)
            return 0;
        L = (L + 1) % k;
        sz--;
        return 1;
    }

    bool deleteLast() {
        if (sz == 0)
            return 0;
        sz--;
        return 1;
    }

    int getFront() { return sz == 0 ? -1 : src[L]; }

    int getRear() { return sz == 0 ? -1 : src[(L + sz - 1) % k]; }

    bool isEmpty() { return sz == 0; }

    bool isFull() { return sz == k; }
};

