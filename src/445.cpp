/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
#ifdef RINNE
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
#endif

class Solution {
  public:
    pair<ListNode *, ListNode *> reverse(ListNode *x) {
        if (x->next) {
            auto [f, s] = reverse(x->next);
            s->next = x;
            x->next = nullptr;
            return {f, x};
        } else {
            return {x, x};
        }
    }
    ListNode *addTwoNumbers(ListNode *l1, ListNode *l2) {
        auto head1 = reverse(l1).first;
        auto head2 = reverse(l2).first;
        int lbit = 0;
        ListNode *dummy = new ListNode();
        ListNode *tail = dummy;
        while (head1 || head2 || lbit) {
            int val1 = head1 ? head1->val : 0;
            int val2 = head2 ? head2->val : 0;
            int sum = val1 + val2 + lbit;
            lbit = sum / 10;
            sum %= 10;
            tail->next = new ListNode(sum);
            tail = tail->next;
            tail->next = nullptr;
            if (head1)
                head1 = head1->next;
            if (head2)
                head2 = head2->next;
        }
        auto ret = dummy->next;
        delete dummy;
        auto f = reverse(ret).first;
        return f;
    }
};

