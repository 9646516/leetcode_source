int dp[12][1 << 12];
class Solution {
  public:
    int shortestPathLength(vector<vector<int>> &V) {
        const int N = V.size();
        memset(dp, 0x3f, sizeof(dp));
        queue<tuple<int, int, int>> st;
        for (int i = 0; i < N; i++) {
            st.push({0, i, 1 << i});
            dp[i][1 << i] = 0;
        }
        while (!st.empty()) {
            auto [d, pos, mask] = st.front();
            st.pop();
            for (int i : V[pos]) {
                int mask2 = mask | 1 << i;
                if (dp[i][mask2] > d + 1) {
                    dp[i][mask2] = d + 1;
                    st.push({d + 1, i, mask2});
                }
            }
        }
        int ans = INF;
        for (int i = 0; i < N; i++) {
            ans = min(ans, dp[i][(1 << N) - 1]);
        }
        return ans;
    }
};

