class MyCircularQueue {
  public:
    vector<int> V;
    int L;
    int R;
    int sz;
    int n;
    MyCircularQueue(int k) {
        V.resize(k);
        L = 0;
        R = 0;
        sz = 0;
        n = k;
    }

    bool enQueue(int value) {
        if (sz == n)
            return 0;
        V[R] = value;
        R++;
        sz++;
        if (R >= n)
            R -= n;
        return 1;
    }

    bool deQueue() {
        if (sz == 0)
            return 0;
        sz--;
        L++;
        if (L >= n)
            L -= n;
        return 1;
    }

    int Front() {
        if (sz == 0)
            return -1;
        return V[L];
    }

    int Rear() {
        if (sz == 0)
            return -1;
        int idx = R - 1;
        if (idx < 0)
            idx += n;
        return V[idx];
    }

    bool isEmpty() { return sz == 0; }

    bool isFull() { return sz == n; }
};

