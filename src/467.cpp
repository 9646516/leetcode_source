class Solution {
  public:
    int findSubstringInWraproundString(string p) {
        vector<int> cnt(26);
        char pre = p[0] - 'a';
        int len = 1;
        auto check = [&]() {
            int now = pre;
            int now_len = len;
            while (now_len) {
                cnt[now] = max(cnt[now], now_len);
                if (now == 25)
                    now = 0;
                else
                    now++;
                now_len--;
                if (now == pre)
                    break;
            }
        };
        for (int i = 1; i < (int)p.size(); i++) {
            int val = p[i] - 'a';
            int need = p[i - 1] - 'a' + 1;
            if (need == 26)
                need = 0;
            if (val == need) {
                len++;
            } else {
                check();
                len = 1;
                pre = val;
            }
        }
        check();
        return accumulate(cnt.begin(), cnt.end(), 0);
    }
};

