class Solution {
  public:
    bool containsNearbyAlmostDuplicate(vector<int> &nums, int k, int t) {
        multiset<int64_t> st;
        for (int _i = 0; _i < nums.size(); _i++) {
            int64_t i = nums[_i];
            int64_t L = i - t;
            int64_t R = i + t;
            auto ptr = st.lower_bound(L);
            if (ptr != st.end() && *ptr >= L && *ptr <= R)
                return true;
            st.insert(i);
            while (st.size() > k) {
                int top = nums[_i - st.size() + 1];
                st.erase(st.lower_bound(top));
            }
        }
        return false;
    }
};

