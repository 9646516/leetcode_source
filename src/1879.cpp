class Solution {
  public:
    int minimumXORSum(vector<int> &a, vector<int> &b) {
        vector<int> old_dp(1 << a.size());
        vector<int> new_dp(1 << a.size());
        for (int i = 0; i < a.size(); i++) {
            fill(new_dp.begin(), new_dp.end(), INF);
            for (int j = 0; j < (int)old_dp.size(); j++) {
                for (int k = 0; k < b.size(); k++) {
                    if (((j >> k) & 1) == 0) {
                        new_dp[j | 1 << k] = min(new_dp[j | 1 << k], old_dp[j] + (a[i] ^ b[k]));
                    }
                }
            }
            swap(old_dp, new_dp);
        }
        return old_dp.back();
    }
};

