class Solution {
  public:
    bool canPartition(vector<int> &nums) {
        int sum = 0;
        for (int i : nums)
            sum += i;
        if (sum & 1)
            return false;
        sum /= 2;
        static int dp[30000];
        memset(dp, 0, sizeof(dp));
        dp[0] = 1;
        for (int i : nums) {
            for (int j = 20000 - i; j >= 0; j--) {
                dp[i + j] |= dp[j];
            }
        }
        return dp[sum];
    }
};

