class Solution {
  public:
    bool isScramble(const string &s1, const string &s2) {
        static int dp[31][31][31];
        memset(dp, -1, sizeof(dp));
        function<bool(int, int, int)> gao = [&](int x, int y, int len) -> bool {
            if (dp[x][y][len] != -1) {
                return dp[x][y][len];
            } else if (!len) {
                return true;
            } else if (len == 1) {
                return dp[x][y][len] = s1[x] == s2[y];
            } else {
                for (int i = 1; i < len; i++) {
                    if ((gao(x, y, i) && gao(x + i, y + i, len - i)) ||
                        (gao(x + len - i, y, i) && gao(x, y + i, len - i))) {
                        return dp[x][y][len] = true;
                    }
                }
                return dp[x][y][len] = false;
            }
        };
        return gao(0, 0, s1.size());
    }
};

