class Solution {
  public:
    string fractionToDecimal(int _f, int _s) {
        if (_f == 0)
            return "0";
        int64_t f = _f;
        int64_t s = _s;
        stringstream ss;
        if (f > 0 != s > 0) {
            f = abs(f);
            s = abs(s);
            ss << '-';
        }
        ss << f / s;
        f -= f / s * s;
        if (f)
            ss << '.';
        string sb = ss.str();
        map<int, int> mp;
        while (f) {
            if (mp.count(f))
                break;
            else {
                mp[f] = sb.size();
                f = f * 10;
                sb.push_back(f / s + '0');
                f -= f / s * s;
            }
        }
        if (f) {
            string p1 = sb.substr(0, mp[f]);
            string p2 = sb.substr(mp[f], sb.size() - mp[f]);
            stringstream res;
            res << p1 << '(' << p2 << ')';
            return res.str();
        } else {
            return sb;
        }
    }
};

