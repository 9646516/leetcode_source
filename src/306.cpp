class Solution {
  public:
    string_view str;
    bool dfs(int pos, int idx, int64_t f, int64_t s) {
        debug(pos, idx, f, s);
        if (pos == str.size()) {
            return idx >= 3;
        } else {
            const int mx = min((int)str.size(), pos + 18);
            int64_t val = 0;
            for (int i = pos; i < mx; i++) {
                if (str[pos] == '0' && i > pos)
                    break;
                val = val * 10 + str[i] - '0';
                if (idx < 2 || f + s == val) {
                    if (dfs(i + 1, idx + 1, s, val)) {
                        return true;
                    }
                }
            }
            return false;
        }
    }
    bool isAdditiveNumber(string num) {
        str = num;
        return dfs(0, 0, 0, 0);
    }
};

