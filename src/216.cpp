class Solution {
  public:
    vector<vector<int>> ret;
    vector<int> stack;
    void dfs(int n, int k, int mx) {
        if (n == 0) {
            if (k == 0) {
                ret.push_back(stack);
            }
        } else if (mx < k) {
            return;
        } else {
            int rr = max(1, k - 1);
            for (int i = mx; i >= rr; i--) {
                stack.push_back(i);
                dfs(n - i, k - 1, i - 1);
                stack.pop_back();
            }
        }
    }
    vector<vector<int>> combinationSum3(int k, int n) {
        dfs(n, k, 9);
        return ret;
    }
};

