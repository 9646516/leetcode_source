class WordDictionary {
  public:
    int sz;
    bitset<10000 + 5> st[26][26][26];
    bitset<10000 + 5> res;
    vector<int> cnt;
    WordDictionary() {
        sz = 0;
        cnt.resize(26);
        for (int i = 0; i < 26; i++) {
            for (int j = 0; j < 26; j++) {
                for (int k = 0; k < 26; k++) {
                    st[i][j][k].reset();
                }
            }
        }
    }
    void addWord(const string &word) {
        for (int i = 0; i < (int)word.size(); i++) {
            int val = word[i] - 'a';
            st[word.size()][i][val][sz] = 1;
        }
        cnt[word.size()]++;
        sz++;
    }

    bool search(const string &word) {
        if (cnt[word.size()] == 0)
            return false;
        else {
            res.set();
            for (int i = 0; i < (int)word.size(); i++) {
                if (word[i] != '.') {
                    int val = word[i] - 'a';
                    res &= st[word.size()][i][val];
                    if (!res.any())
                        return false;
                }
            }
            return res.any();
        }
    }
};

