class Solution {
  public:
    vector<int> sumPrefixScores(vector<string> &words) {
        unordered_map<ll, int> mp[1005];
        for (auto &i : words) {
            ll a = 0, b = 0;
            for (int j=0;j<i.size();j++) {
                a = a * 233 + i[j];
                b = b * 2333 + i[j];
                a%=MOD;
                b%=MOD;
                mp[j][a<<32|b]++;
            }
        }
        vector<int> ret;
        for (auto &i : words) {
            int ans = 0;
            ll a = 0, b = 0;
            for (int j=0;j<i.size();j++) {
                a = a * 233 + i[j];
                b = b * 2333 + i[j];
                a%=MOD;
                b%=MOD;
                ans += mp[j][a<<32|b];
            }
            ret.push_back(ans);
        }
        return ret;
    }
};
