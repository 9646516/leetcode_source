class Solution {
  public:
    vector<vector<string>> findDuplicate(vector<string> &paths) {
        unordered_map<string, vector<string>> mp;
        for (auto &i : paths) {
            stringstream ss(i);
            string base, b;
            ss >> base;
            while (ss >> b) {
                string a, c;
                a = base;
                a.push_back('/');
                b.pop_back();
                for (int i = 0; i < (int)b.size(); i++) {
                    if (b[i] == '(') {
                        for (int j = i + 1; j < (int)b.size(); j++) {
                            c.push_back(b[j]);
                        }
                        break;
                    } else {
                        a.push_back(b[i]);
                    }
                }
                cout << c << ' ' << a << endl;
                mp[c].push_back(a);
            }
        }
        vector<vector<string>> ret;
        for (auto &[a, b] : mp) {
            if (b.size() > 1)
                ret.push_back(b);
        }
        return ret;
    }
};

