class Solution {
  public:
    bool matchReplacement(string s, string sub, vector<vector<char>> &mappings) {
        bitset<5000> st[255], ans;
        ans.set();
        vector<vector<int>> V(255);
        for (auto &i : mappings) {
            V[i[1]].push_back(i[0]);
        }
        for (int i = 0; i < (int)s.size(); i++) {
            st[s[i]][i] = 1;
            for (int j : V[s[i]]) {
                st[j][i] = 1;
            }
        }
        for (int i = 0; i < (int)sub.size(); i++) {
            ans &= (st[sub[i]] >> i);
        }
        return ans.any();
    }
};

