class Solution {
  public:
    vector<string> letterCombinations(string digits) {
        if (digits.empty()) {
            return {};
        } else {
            vector<string> ret;
            const vector<vector<char>> ch = {
                {},              // 0
                {},              // 1
                {'a', 'b', 'c'}, // 2
                {'d', 'e', 'f'},
                {'g', 'h', 'i'},
                {'j', 'k', 'l'},
                {'m', 'n', 'o'},
                {'p', 'q', 'r', 's'},
                {'t', 'u', 'v'},
                {'w', 'x', 'y', 'z'},
            };
            string stack;
            stack.reserve(digits.size());
            function<void(int)> solve = [&](int idx) -> void {
                if (idx == digits.size()) {
                    ret.push_back(stack);
                } else {
                    int p = digits[idx] - '0';
                    for (int i = 0; i < (int)ch[p].size(); i++) {
                        stack.push_back(ch[p][i]);
                        solve(idx + 1);
                        stack.pop_back();
                    }
                }
            };
            solve(0);
            return ret;
        }
    }
};

