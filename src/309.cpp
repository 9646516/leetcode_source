class Solution {
  public:
    int maxProfit(vector<int> &V) {
        vector<int> dp(3), dp2(3);
        // 0 没有持有 1 持有 2 冷却
        dp[0] = 0;
        dp[1] = -V[0];
        dp[2] = 0;
        for (int i = 1; i < (int)V.size(); i++) {
            dp2[0] = max(dp[0], dp[2]);
            dp2[1] = max(dp[1], dp[0] - V[i]);
            dp2[2] = dp[1] + V[i];
            swap(dp, dp2);
        }
        return max({dp[0], dp[2]});
    }
};

