class Solution {
  public:
    vector<string> now;
    vector<vector<string>> ret;
    string_view str;
    bool ok(const string &x) {
        for (int i = 0;; i++) {
            int r = x.size() - 1 - i;
            if (i > r)
                break;
            if (x[i] != x[r])
                return false;
        }
        return true;
    }
    void dfs(int x) {
        if (x == str.size()) {
            ret.push_back(now);
        } else {
            string sb;
            for (int i = x; i < (int)str.size(); i++) {
                sb.push_back(str[i]);
                if (ok(sb)) {
                    now.push_back(sb);
                    dfs(i + 1);
                    now.pop_back();
                }
            }
        }
    }
    vector<vector<string>> partition(string s) {
        str = s;
        dfs(0);
        return ret;
    }
};

