class Solution {
  public:
    long long maxRunTime(int n, vector<int> &V) {
        ll L = 0, R = accumulate(V.begin(), V.end(), 0LL) / n, ans = 0;
        while (L <= R) {
            ll mid = (L + R) / 2;
            ll xs = 0;
            for (int i : V) {
                xs += min(1LL * i, mid);
            }
            xs /= n;
            if (xs >= mid) {
                ans = mid;
                L = mid + 1;
            } else {
                R = mid - 1;
            }
        }
        return ans;
    }
};

