class Solution {
  public:
    vector<int> majorityElement(vector<int> &nums) {
        vector<int> ret;
        unordered_map<int, int> mp;
        const int K = nums.size() / 3;
        for (int i : nums)
            mp[i]++;
        for (auto [a, b] : mp)
            if (b > K)
                ret.push_back(a);
        return ret;
    }
};

