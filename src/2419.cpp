class Solution {
  public:
    int longestSubarray(vector<int> &nums) {
        int mx = *max_element(nums.begin(), nums.end());
        int ans = 0, now = 0, pre = -1;
        for (int i : nums) {
            if (i == mx) {
                if (pre == mx) {
                    now++;
                } else {
                    now = 1;
                }
            } else {
                now = 0;
            }
            ans = max(ans, now);
            pre=i;
        }
        return ans;
    }
};
