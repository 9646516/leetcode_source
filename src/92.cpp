class Solution {
  public:
    using node = ListNode;
    void gao(node *first, node *second) {
        if (first != second) {
            node *prev = first;
            for (node *i = first->next; i != second;) {
                node *to = i->next;
                i->next = prev;
                prev = i;
                i = to;
            }
            second->next = prev;
        }
    }
    ListNode *reverseBetween(ListNode *head, int left, int right) {
        if (!head)
            return nullptr;
        else if (!head->next)
            return head;
        else {
            node *dummy = new node();
            dummy->next = head;

            node *first = dummy;
            for (int i = 1; i < left; i++) {
                first = first->next;
            }
            node *start = first->next;
            node *end = start;
            for (int i = 0; i < right - left; i++) {
                end = end->next;
            }
            node *second = end->next;
            cout << start << ' ' << end << endl;
            cout << start->val << ' ' << end->val << endl;
            gao(start, end);
            if (first == dummy) {
                start->next = second;
                delete dummy;
                return end;
            } else {
                first->next = end;
                start->next = second;
                delete dummy;
                return head;
            }
        }
    }
};

