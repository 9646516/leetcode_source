class Solution {
  public:
    using node = TreeNode;
    TreeNode *sortedListToBST(ListNode *head) {
        function<node *(ListNode *, int)> gao = [&](ListNode *head, int len) -> node * {
            if (len == 0)
                return nullptr;
            else if (len == 1) {
                return new node(head->val);
            } else {
                ListNode *mid = head;
                {
                    int k = len / 2;
                    while (k--) {
                        mid = mid->next;
                    }
                }

                node *root = new node(mid->val);
                root->left = gao(head, len / 2);
                root->right = gao(mid->next, len - len / 2 - 1);
                return root;
            }
        };
        int len = 0;
        ListNode *now = head;
        while (now) {
            now = now->next;
            len++;
        }
        return gao(head, len);
    }
};

