class Solution {
  public:
    vector<int> largestDivisibleSubset(vector<int> &V) {
        vector<int> dp(V.size(), 1);
        sort(V.begin(), V.end());
        for (int i = 0; i < (int)V.size(); i++) {
            for (int j = i + 1; j < (int)V.size(); j++) {
                if (V[j] % V[i] == 0) {
                    dp[j] = max(dp[j], dp[i] + 1);
                }
            }
        }
        int pos = max_element(dp.begin(), dp.end()) - dp.begin();
        int val = dp[pos];
        vector<int> ret;
        while (val) {
            ret.push_back(V[pos]);
            val--;
            for (int i = pos - 1; i >= 0; i--) {
                if (dp[i] == val && V[pos] % V[i] == 0) {
                    pos = i;
                    break;
                }
            }
        }
        return ret;
    }
};

