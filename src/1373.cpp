struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};
#endif

class Solution {
  public:
    int ans;
    tuple<bool, int, int, int> dfs(TreeNode *x) {
        bool ok = 1;
        int mx = x->val, mn = x->val, sum = x->val;
        if (x->left) {
            auto [ok2, mx2, mn2, sum2] = dfs(x->left);
            sum += sum2;
            if (ok2 && x->val > mx2) {
                mx = max(mx, mx2);
                mn = min(mn, mn2);
            } else {
                ok = 0;
            }
        }
        if (x->right) {
            auto [ok2, mx2, mn2, sum2] = dfs(x->right);
            sum += sum2;
            if (ok2 && x->val < mn2) {
                mx = max(mx, mx2);
                mn = min(mn, mn2);
            } else {
                ok = 0;
            }
        }
        if (ok) {
            ans = max(ans, sum);
        }
        return {ok, mx, mn, sum};
    };
    int maxSumBST(TreeNode *root) {
        ans = 0;
        dfs(root);
        return ans;
    }
};

