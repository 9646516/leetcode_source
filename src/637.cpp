#ifdef RINNE
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};
#endif
class Solution {
  public:
    int dfs(TreeNode *x) {
        if (!x)
            return 0;
        return max(dfs(x->left), dfs(x->right)) + 1;
    }
    vector<ll> xs;
    vector<int> cnt;
    void dfs2(TreeNode *x, int d) {
        if (!x)
            return;
        xs[d] += x->val;
        cnt[d]++;
        dfs2(x->left, d + 1);
        dfs2(x->right, d + 1);
    }
    vector<double> averageOfLevels(TreeNode *root) {
        int d = dfs(root);
        cnt.resize(d);
        xs.resize(d);
        dfs2(root, 0);
        vector<double> ret;
        for (int i = 0; i < d; i++) {
            ret.push_back(1.0 * xs[i] / cnt[i]);
        }
        return ret;
    }
};

