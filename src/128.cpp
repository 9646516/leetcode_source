class Solution {
  public:
    int longestConsecutive(vector<int> &V) {
        unordered_set<int> st;
        for (auto i : V)
            st.insert(i);
        int ans = 0;
        for (auto i : st) {
            if (!st.count(i - 1)) {
                int len = 0;
                for (int j = i;; j++) {
                    if (st.count(j)) {
                        len++;
                    } else {
                        break;
                    }
                }
                ans = max(ans, len);
            }
        }
        return ans;
    }
};

