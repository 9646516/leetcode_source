class Solution {
  public:
    tuple<int, int, int> split(string &x) {
        vector<string> V;
        string now;
        for (auto i : x) {
            if (i == ':') {
                V.push_back(now);
                now.clear();
            } else {
                now.push_back(i);
            }
        }
        int id = stol(V[0]);
        int start = V[1][0] == 's';
        int time = stol(now);
        return {id, start, time};
    }
    vector<int> exclusiveTime(int n, vector<string> &logs) {
        vector<int> ret(n);
        stack<pair<int, int>> st; // id time
        for (auto &i : logs) {
            auto [id, start, time] = split(i);
            if (start) {
                st.push({id, time});
            } else {
                auto [a, b] = st.top();
                st.pop();
                assert(id == a);
                ret[id] += time - b + 1;
                if (!st.empty()) {
                    int idx = st.top().first;
                    ret[idx] -= time - b + 1;
                }
            }
        }
        return ret;
    }
};

