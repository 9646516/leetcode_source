class Solution {
  public:
    int n;
    int target;
    unordered_map<int, int> dp[305];
    bool dfs(int xs, int mask) {
        if (dp[xs].count(mask))
            return dp[xs][mask];
        else {
            for (int i = 1; i <= n; i++) {
                if (!((mask >> i) & 1)) {
                    int mask2 = mask | 1 << i;
                    int xs2 = xs + i;
                    if (xs2 >= target || !dfs(xs2, mask2)) {
                        return dp[xs][mask] = 1;
                    }
                }
            }
            return dp[xs][mask] = 0;
        }
    }
    bool canIWin(int n, int target) {
        this->n = n;
        this->target = target;
        if (target == 0)
            return 1;
        int xs = (1 + n) * n / 2;
        if (xs < target)
            return false;
        else {

            return dfs(0, 0);
        }
    }
};

