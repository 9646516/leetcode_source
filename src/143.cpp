#ifdef RINNE
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
#endif

class Solution {
  public:
    using node = ListNode;
    void reorderList(ListNode *head) {
        node *dummy = new node();
        auto tail = dummy;
        auto now = head;
        vector<node *> q;
        while (now) {
            q.push_back(now);
            now = now->next;
        }
        int L = 0, R = q.size() - 1;
        while (R - L + 1 >= 2) {
            auto l = q[L++];
            auto r = q[R--];
            tail->next = l;
            l->next = r;
            tail = r;
        }
        if (L <= R) {
            tail->next = q[L];
            tail = tail->next;
        }
        tail->next = nullptr;
        head = dummy->next;
        delete dummy;
    }
};

