class Solution {
  public:
    map<string, int> mp;
    vector<string> V;
    // Encodes a URL to a shortened URL.
    string encode(string longUrl) {
        if (mp.count(longUrl))
            return to_string(mp[longUrl]);
        else {
            V.push_back(longUrl);
            mp[longUrl] = V.size() - 1;
            return to_string(V.size() - 1);
        }
    }

    // Decodes a shortened URL to its original URL.
    string decode(string shortUrl) {
        int idx = stol(shortUrl);
        return V[idx];
    }
};

