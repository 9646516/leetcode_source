class Solution {
  public:
    bool checkDistances(string s, vector<int> &distance) {
        for (int i = 0; i < 26; i++) {
            vector<int> V;
            for (int j = 0; j < (int)s.size(); j++) {
                if (s[j] - 'a' == i) {
                    V.push_back(j);
                }
            }
            for (int j = 1; j < (int)V.size(); j++) {
                if (V[j] - V[j - 1] != distance[i] + 1)
                    return 0;
            }
        }
        return 1;
    }
};
