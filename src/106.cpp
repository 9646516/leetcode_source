struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};
#endif
class Solution {
  public:
    using node = TreeNode;
    TreeNode *buildTree(vector<int> &mid, vector<int> &after) {
        function<node *(int, int, int, int)> gao = [&](int l1, int r1, int l2, int r2) -> node * {
            if (l1 > r1) {
                return nullptr;
            } else if (l1 == r1) {
                return new node(mid[l1]);
            } else {
                int root_val = after[r2];
                int split = -1;
                for (int i = l1; i <= r1; i++) {
                    if (mid[i] == root_val) {
                        split = i;
                        break;
                    }
                }
                int len1 = split - l1;
                node *root = new node(root_val);
                root->left = gao(l1, split - 1, l2, l2 + len1 - 1);
                root->right = gao(split + 1, r1, l2 + len1, r2 - 1);
                return root;
            }
        };
        return gao(0, (int)mid.size() - 1, 0, (int)after.size() - 1);
    }
};

