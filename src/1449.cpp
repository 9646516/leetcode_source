class Solution {
  public:
    string dp[5005];
    vector<int> V;
    string dfs(int x) {
        if (x == 0)
            return "";
        else if (!dp[x].empty())
            return dp[x];
        else {
            string ret = "";
            for (int i = 9; i >= 1; i--) {
                if (x >= V[i - 1]) {
                    auto res = dfs(x - V[i - 1]);
                    if (res == "0")
                        continue;
                    res.insert(res.begin(), '0' + i);
                    if (ret.size() < res.size() || (ret.size() == res.size() && ret < res)) {
                        ret = res;
                    }
                }
            }
            if (ret == "")
                return dp[x] = "0";
            else
                return dp[x] = ret;
        }
    }
    string largestNumber(vector<int> &cost, int target) {
        V = cost;
        return dfs(target);
    }
};

