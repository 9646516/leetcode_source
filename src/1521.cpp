template <typename T, class F = function<T(const T &, const T &)>> class SparseTable {
  public:
    int n;
    vector<vector<T>> mat;
    F func;
    SparseTable(const vector<T> &a, const F &f) : func(f) {
        n = static_cast<int>(a.size());
        int max_log = 32 - __builtin_clz(n);
        mat.resize(max_log);
        mat[0] = a;
        for (int j = 1; j < max_log; j++) {
            mat[j].resize(n - (1 << j) + 1);
            for (int i = 0; i <= n - (1 << j); i++) {
                mat[j][i] = func(mat[j - 1][i], mat[j - 1][i + (1 << (j - 1))]);
            }
        }
    }
    T get(int from, int to) const {
        assert(0 <= from && from <= to && to <= n - 1);
        int lg = 32 - __builtin_clz(to - from + 1) - 1;
        return func(mat[lg][from], mat[lg][to - (1 << lg) + 1]);
    }
};
class Solution {
  public:
    int closestToTarget(vector<int> &arr, int target) {
        SparseTable<int> max_st(arr, [&](int i, int j) { return (i & j); });
        int ans = INF;
        auto solve = [&](int l, int r) {
            int xs = max_st.get(l, r);
            ans = min(ans, abs(target - xs));
        };
        for (int i = 0; i < (int)arr.size(); i++) {
            int L = i, R = arr.size() - 1, ans = -1;
            while (L <= R) {
                int mid = (L + R) / 2;
                if (max_st.get(i, mid) < target) {
                    ans = mid;
                    R = mid - 1;
                } else {
                    L = mid + 1;
                }
            }
            if (ans == -1)
                solve(i, arr.size() - 1);
            else {
                solve(i, ans);
                if (ans != i)
                    solve(i, ans - 1);
            }
        }
        return ans;
    }
};

