class NumMatrix {
  public:
    vector<vector<int>> V;
    const int n;
    const int m;
    NumMatrix(vector<vector<int>> &matrix) : V(matrix), n(matrix.size()), m(matrix.front().size()) {
        for (int i = 0; i < n; i++) {
            for (int j = 1; j < m; j++) {
                V[i][j] += V[i][j - 1];
            }
        }
        for (int j = 0; j < m; j++) {
            for (int i = 1; i < n; i++) {
                V[i][j] += V[i - 1][j];
            }
        }
    }

    int sumRegion(int row1, int col1, int row2, int col2) {
        int ret = V[row2][col2];
        if (row1)
            ret -= V[row1 - 1][col2];
        if (col1)
            ret -= V[row2][col1 - 1];
        if (row1 && col1)
            ret += V[row1 - 1][col1 - 1];
        return ret;
    }
};

