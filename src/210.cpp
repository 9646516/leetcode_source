class Solution {
  public:
    vector<int> findOrder(int n, vector<vector<int>> &V) {
        vector<int> cnt(n);
        vector<vector<int>> edge(n);
        for (auto &i : V) {
            edge[i[1]].push_back(i[0]);
            cnt[i[0]]++;
        }
        queue<int> q;
        for (int i = 0; i < n; i++) {
            if (!cnt[i])
                q.push(i);
        }
        vector<int> ret;
        while (!q.empty()) {
            int now = q.front();
            q.pop();
            ret.push_back(now);
            for (int i : edge[now]) {
                cnt[i]--;
                if (cnt[i] == 0)
                    q.push(i);
            }
        }
        if (ret.size() != n)
            return {};
        else
            return ret;
    }
};

