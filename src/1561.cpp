class Solution {
  public:
    int maxCoins(vector<int> &piles) {
        sort(piles.begin(), piles.end());
        deque<int> q;
        for (auto i : piles)
            q.push_back(i);
        int ret = 0;
        while (!q.empty()) {
            q.pop_back();
            q.pop_front();
            ret += q.back();
            q.pop_back();
        }
        return ret;
    }
};
