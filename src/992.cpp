class Solution {
  public:
    int subarraysWithKDistinct(vector<int> &nums, int k) {
        auto solve = [&](int val) {
            if (val == 0)
                return 0;
            int pos = 0;
            int ret = 0;
            unordered_map<int, int> mp;
            for (int i = 0; i < nums.size(); i++) {
                while (pos < nums.size()) {
                    if (mp.size() == val && !mp.count(nums[pos]))
                        break;
                    mp[nums[pos]]++;
                    pos++;
                }
                ret += pos - i;
                mp[nums[i]]--;
                if (mp[nums[i]] == 0) {
                    mp.erase(nums[i]);
                }
            }
            return ret;
        };
        return solve(k) - solve(k - 1);
    }
};

