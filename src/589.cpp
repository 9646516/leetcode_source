class Node {
  public:
    int val;
    vector<Node *> children;

    Node() {}

    Node(int _val) { val = _val; }

    Node(int _val, vector<Node *> _children) {
        val = _val;
        children = _children;
    }
};
#endif

class Solution {
  public:
    vector<int> V;
    void dfs(Node *x) {
        V.push_back(x->val);
        for (auto i : x->children) {
            dfs(i);
        }
    }
    vector<int> preorder(Node *root) {
        if (root)
            dfs(root);

        return V;
    }
};

