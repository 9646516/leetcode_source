class Solution {
  public:
    void merge(vector<int> &V1, const int n, const vector<int> &V2, const int m) {
        vector<int> packed;
        int ptr1 = 0, ptr2 = 0;
        while (ptr1 < n && ptr2 < m) {
            if (V1[ptr1] < V2[ptr2]) {
                packed.push_back(V1[ptr1++]);
            } else {
                packed.push_back(V2[ptr2++]);
            }
        }
        while (ptr1 < n) {
            packed.push_back(V1[ptr1++]);
        }
        while (ptr2 < m) {
            packed.push_back(V2[ptr2++]);
        }
        V1 = move(packed);
    }
};

