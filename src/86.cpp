/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
// 输入：head = [1,4,3,2,5,2], x = 3
// 输出：[1,2,2,4,3,5]
#ifdef RINNE
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
#endif
class Solution {
  public:
    using node = ListNode;
    ListNode *partition(ListNode *head, int x) {
        node *less = nullptr;
        node *less_tail = nullptr;
        node *greater = nullptr;
        node *greater_tail = nullptr;
        node *now = head;
        while (now) {
            if (now->val < x) {
                if (less_tail) {
                    less_tail->next = now;
                    less_tail = less_tail->next;
                } else {
                    less = now;
                    less_tail = now;
                }
            } else {
                if (greater_tail) {
                    greater_tail->next = now;
                    greater_tail = greater_tail->next;
                } else {
                    greater = now;
                    greater_tail = now;
                }
            }
            now = now->next;
        }
        if (greater) {
            greater_tail->next = nullptr;
            if (less_tail) {
                less_tail->next = greater;
            } else {
                less = greater;
            }
        }
        return less;
    }
};

