struct node {
    int dep;
    int val;
};
class Solution {
  public:
    deque<node> V;
    TreeNode *dfs(int dep) {
        if (V.empty() || V[0].dep != dep)
            return nullptr;
        else {
            TreeNode *ret = new TreeNode(V[0].val);
            V.pop_front();
            ret->left = dfs(dep + 1);
            ret->right = dfs(dep + 1);
            return ret;
        }
    }
    TreeNode *recoverFromPreorder(string traversal) {
        int dep = 0;
        for (int i = 0; i < (int)traversal.size();) {
            if (traversal[i] == '-') {
                dep++;
                i++;
            } else {
                int val = 0;
                int j = i;
                for (; j < (int)traversal.size(); j++) {
                    if (isdigit(traversal[j])) {
                        val = val * 10 + traversal[j] - '0';
                    } else {
                        break;
                    }
                }
                i = j;
                V.push_back({dep, val});
                dep = 0;
            }
        }
        return dfs(0);
    }
};

