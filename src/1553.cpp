class Solution {
  public:
    unordered_map<int, int> mp;
    int minDays(int n) {
        if (n <= 2)
            return n;
        if (mp.count(n))
            return mp[n];
        else
            return mp[n] = min(minDays(n / 2) + n % 2, minDays(n / 3) + n % 3) + 1;
    }
};

