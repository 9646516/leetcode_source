int dp[50000 + 5];
class Solution {
  public:
    string stoneGameIII(vector<int> &V) {
        const int n = V.size();
        dp[n] = 0;
        dp[n - 1] = V[n - 1];
        for (int i = n - 2; i >= 0; i--) {
            int sum = 0;
            dp[i] = -INF;
            for (int j = 0; j < 3; j++) {
                if (i + j >= n)
                    break;
                sum += V[i + j];
                dp[i] = max(dp[i], sum - dp[i + j + 1]);
            }
        }
        if (dp[0] == 0)
            return "Tie";
        else if (dp[0] > 0)
            return "Alice";
        else
            return "Bob";
    }
};

