class Solution {
  public:
    unordered_set<int> st;
    int n, m;
    mt19937 rng;
    uniform_int_distribution<int> X;
    Solution(int n, int m) {
        this->n = n;
        this->m = m;
        X = uniform_int_distribution<int>(0, n * m - 1);
        rng = mt19937(time(0));
    }

    vector<int> flip() {
        while (true) {
            int idx = X(rng);
            if (!st.count(idx)) {
                st.insert(idx);
                int i = idx / m;
                int j = idx % m;
                return {i, j};
            }
        }
    }

    void reset() { st.clear(); }
};

