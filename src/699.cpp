class Solution {
  public:
    vector<int> fallingSquares(vector<vector<int>> &positions) {
        vector<int> H;
        for (auto &i : positions) {
            int pos = i[0];
            int len = i[1];
            H.push_back(pos);
            H.push_back(pos + len - 1);
        }
        sort(H.begin(), H.end());
        H.erase(unique(H.begin(), H.end()), H.end());
        int val[2005];
        memset(val, 0, sizeof(val));
        vector<int> ret;
        for (auto &i : positions) {
            int pos = i[0];
            int len = i[1];
            int L = lower_bound(H.begin(), H.end(), pos) - H.begin();
            int R = lower_bound(H.begin(), H.end(), pos + len - 1) - H.begin();
            int mx = *max_element(val + L, val + R + 1);
            for (int j = L; j <= R; j++) {
                val[j] = mx + len;
            }
            if (ret.empty())
                ret.push_back(mx + len);
            else {
                ret.push_back(max(ret.back(), mx + len));
            }
        }
        return ret;
    }
};

