int dp[15][1 << 15];
class Solution {
  public:
    int n;
    vector<int> V[20];
    int dfs(int x, int mask) {
        if (x >= this->n) {
            return 1;
        } else if (dp[x][mask] != -1) {
            return dp[x][mask];
        } else {
            int ans = 0;
            for (int i : V[x + 1]) {
                int lb = i - 1;
                if ((mask >> lb) & 1)
                    continue;
                ans += dfs(x + 1, mask | 1 << lb);
            }
            return dp[x][mask] = ans;
        }
    }
    int countArrangement(int n) {
        this->n = n;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                if (i % j == 0 || j % i == 0) {
                    V[i].push_back(j);
                }
            }
        }
        memset(dp, -1, sizeof(dp));
        return dfs(0, 0);
    }
};

