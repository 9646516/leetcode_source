const int MOD = 1e9 + 7;
class Solution {
  public:
    int rangeSum(vector<int> &nums, int n, int left, int right) {
        vector<ll> prefix;
        for (int i : nums) {
            prefix.push_back(i);
        }
        for (int i = 1; i < (int)prefix.size(); i++) {
            prefix[i] += prefix[i - 1];
        }
        vector<ll> st;
        for (int i = 0; i < (int)prefix.size(); i++) {
            for (int j = i; j < (int)prefix.size(); j++) {
                ll sum = prefix[j];
                if (i)
                    sum -= prefix[i - 1];
                st.push_back(sum);
            }
        }
        sort(st.begin(), st.end());
        debug(st);
        ll ans = 0;
        for (int i = left; i <= right; i++) {
            ans = (ans + st[i - 1]) % MOD;
        }
        return ans;
    }
};
