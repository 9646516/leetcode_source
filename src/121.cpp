class Solution {
  public:
    int maxProfit(vector<int> &V) {
        int ret = 0;
        int suffix = V.back();
        for (int i = (int)V.size() - 2; i >= 0; i--) {
            ret = max(ret, suffix - V[i]);
            suffix = max(suffix, V[i]);
        }
        return ret;
    }
};

