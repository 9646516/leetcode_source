struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
#endif
class Solution {
  public:
    using node = ListNode;
    ListNode *deleteDuplicates(ListNode *head) {
        node *ret = nullptr;
        node *tail = nullptr;
        node *now = head;
        while (now) {
            if (now->next) {
                if (now->val != now->next->val) {
                    if (ret) {
                        tail->next = now;
                        tail = tail->next;
                    } else {
                        ret = now;
                        tail = ret;
                    }
                    now = now->next;
                } else {
                    node *to_remove;
                    int bad = now->val;
                    while (now && now->val == bad) {
                        to_remove = now;
                        now = now->next;
                        delete to_remove;
                    }
                }
            } else {
                if (ret) {
                    tail->next = now;
                    tail = tail->next;
                } else {
                    ret = now;
                    tail = ret;
                }
                break;
            }
        }
        if (tail) {
            tail->next = nullptr;
        }
        return ret;
    }
};

