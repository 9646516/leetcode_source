class MyStack {
  public:
    queue<int> q, q2;
    MyStack() {}

    void push(int x) { q.push(x); }

    int pop() {
        while (q.size() > 1) {
            q2.push(q.front());
            q.pop();
        }
        int ret = q.front();
        q.pop();
        while (!q2.empty()) {
            q.push(q2.front());
            q2.pop();
        }
        return ret;
    }

    int top() {
        while (q.size() > 1) {
            q2.push(q.front());
            q.pop();
        }
        int ret = q.front();
        q.pop();
        q2.push(ret);
        while (!q2.empty()) {
            q.push(q2.front());
            q2.pop();
        }
        return ret;
    }

    bool empty() { return q.empty(); }
};

