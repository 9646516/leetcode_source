class Solution {
  public:
    vector<int> p;
    mt19937 rng;
    uniform_int_distribution<int> gen;
    Solution(vector<int> &w) {
        p = w;
        for (int i = 1; i < p.size(); i++) {
            p[i] += p[i - 1];
        }
        this->rng = mt19937(114514);
        this->gen = uniform_int_distribution<int>(1, p.back());
    }

    int pickIndex() {
        int idx = this->gen(this->rng);
        return lower_bound(p.begin(), p.end(), idx) - p.begin();
    }
};

