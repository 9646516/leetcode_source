#ifdef RINNE
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};
#endif

class Solution {
  public:
    int depth, val;
    void dfs(TreeNode *x, int dep) {
        if (dep == this->depth - 1) {
            {
                auto t = new TreeNode(val);
                t->left = x->left;
                x->left = t;
            }
            {
                auto t = new TreeNode(val);
                t->right = x->right;
                x->right = t;
            }
        } else {
            if (x->left)
                dfs(x->left, dep + 1);
            if (x->right)
                dfs(x->right, dep + 1);
        }
    }
    TreeNode *addOneRow(TreeNode *root, int val, int depth) {
        if (depth == 1) {
            auto t = new TreeNode(val);
            t->left = root;
            return t;
        } else {
            this->val = val;
            this->depth = depth;
            dfs(root, 1);
            return root;
        }
    }
};

