class Solution {
  public:
    bool ok(vector<vector<int>> &V, int mask, const int N) {
        for (int i = 0; i < N; i++) {
            int self = (mask >> i) & 1;
            if (self == 0)
                continue;
            for (int j = 0; j < N; j++) {
                if (V[i][j] == 2)
                    continue;
                int rhs = (mask >> j) & 1;
                if (rhs == 0 && V[i][j] == 0)
                    continue;
                if (rhs == 1 && V[i][j] == 1)
                    continue;
                return 0;
            }
        }
        return 1;
    }
    int maximumGood(vector<vector<int>> &V) {
        const int N = V.size();
        int ans = 0;
        for (int mask = 0; mask < (1 << N); mask++) {
            if (ok(V, mask, N)) {
                ans = max(ans, __builtin_popcount(mask));
            }
        }
        return ans;
    }
};

