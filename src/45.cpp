class Solution {
  public:
    int jump(vector<int> &nums) {
        static int dp[10000 + 5];
        memset(dp, -1, sizeof(dp));
        function<int(int)> gao = [&](int now) -> int {
            if (dp[now] != -1)
                return dp[now];
            else if (now == (int)nums.size() - 1) {
                return dp[now] = 0;
            } else {
                int res = INF;
                for (int i = 1; i <= nums[now]; i++) {
                    int next = i + now;
                    if (next >= nums.size())
                        break;
                    else {
                        res = min(res, gao(next));
                    }
                }
                return dp[now] = res + 1;
            }
        };
        return gao(0);
    }
};

