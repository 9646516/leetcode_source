int dp[105][105][105];
class Solution {
  public:
    vector<int> box;
    int dfs(int l, int r, int k) {
        if (l > r) {
            return 0;
        } else if (dp[l][r][k] != -1) {
            return dp[l][r][k];
        } else {
            int ans = dfs(l, r - 1, 0) + (k + 1) * (k + 1);
            for (int i = r - 1; i >= l; i--) {
                if (box[i] == box[r]) {
                    ans = max(ans, dfs(l, i, k + 1) + dfs(i + 1, r - 1, 0));
                }
            }
            return dp[l][r][k] = ans;
        }
    }
    int N;
    int removeBoxes(vector<int> &boxes) {
        box = boxes;
        memset(dp, -1, sizeof(dp));
        N = boxes.size();
        return dfs(0, N - 1, 0);
    }
};

