class Solution {
  public:
    int maxProduct(vector<int> &V) {
        int max_prefix = V[0], min_prefix = V[0];
        int ans = V[0];
        for (int _i = 1; _i < (int)V.size(); _i++) {
            auto i = V[_i];
            auto a = max({max_prefix * i, min_prefix * i, i});
            auto b = min({max_prefix * i, min_prefix * i, i});
            max_prefix = a;
            min_prefix = b;
            ans = max(ans, max_prefix);
        }
        return ans;
    }
};

