class Solution {
  public:
    string removeDuplicateLetters(string s) {
        vector<int> vis(255, 0);
        vector<int> inq(255, 0);
        string now;
        for (int i = 0; i < (int)s.size(); i++) {
            vis[s[i]]++;
        }
        for (int i = 0; i < (int)s.size(); i++) {
            char ch = s[i];
            while (!now.empty() && vis[now.back()] > 1 && now.back() > ch && !inq[ch]) {
                inq[now.back()] = 0;
                vis[now.back()]--;
                now.pop_back();
            }
            if (!inq[ch]) {
                now.push_back(ch);
                inq[ch] = 1;
            } else {
                vis[ch]--;
            }
        }
        return now;
    }
};

