class Solution {
  public:
    int lengthOfLastWord(string s) {
        string now;
        string ret;
        for (int i = 0; i < (int)s.size(); i++) {
            debug(now, ret);
            if (s[i] == ' ') {
                if (!now.empty()) {
                    swap(now, ret);
                    now.clear();
                }
            } else {
                now.push_back(s[i]);
            }
        }
        if (!now.empty())
            swap(now, ret);
        return ret.size();
    }
};

